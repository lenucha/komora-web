<h2>O komoře</h2>
Česká Komora tlumočníku znakového jazyka (Komora) je nezisková
dobrovolná profesní organizace sdružující především tlumočníky
českého znakového jazyka bez ohledu na stav jejich sluchu. Hlavním
posláním Komory je zasazovat se o rozvoj a lepší postavení tlumočnické
profese u nás, čímž zároveň přispívá k naplňování práv a potřeb
neslyšících, nedoslýchavých a ohluchlých lidí. Komora je tu pro
všechny tlumočníky, studenty tlumočení i další sympatizanty (podporovatele),
kteří hledají možnosti vzdělávání, zajímají se o aktuality z
oboru a je pro ně důležité zázemí profesní organizace. Komoru
dělají sami tlumočníci pro ostatní tlumočníky. Členy Komory se
tedy mohou stát i lidé, kteří se tlumočnické profesi nevěnují,
ale zajímá je problematika Neslyšících, jejich kultura a jazyk,
souhlasí s názorovými stanovisky a cíli Komory a podporují její
aktivity - pro ty je tu možnost přidruženého členství.
